let nome // Declarando a variável
nome = 'Rodrigo' // Inicializando a variável

console.log(`${nome} nasceu em 1988.`)
console.log(`Em 2010, ${nome} conheceu a Lorraine.`)
console.log(`${nome} casou-se com Lorraine em 2022.`)
console.log(`Lorraine teve 1 filho com ${nome} em 2025.`)
console.log(`O filho de ${nome} chama-se Miguel.`)

/*  

    - Não podemos criar variáveis com palavras reservadas.
    Ex: let console = '...'

    - As variáveis precisam ter nomes significativos.
    Ex: let nomeCLiente = 'João'

    - Não podemos começar o nome de uma variável com um NÚMERO.
    Ex: let 1nome = 'João'

    - As variáveis não podem conter espaços ou traços.
    Ex: let nome-ClienteRect

    - Podemos utilizar camelCase
    Ex: let nomeCompletoDoCliente = 'Rodrigo Victório da Silva'

    - Case-sensitive
    Ex: let nomeCliente = 'Luiz' 
            é diferente de
        let nomecliente = 'João'

    - Não podemos redeclarar variáveis com LET.
*/


