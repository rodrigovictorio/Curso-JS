/*
    OPERADORES DE COMPARAÇÃO
    - ( > )   - maior que 
    - ( >= )  - maior ou igual a
    - ( < )   - menor que
    - ( <= )  - menor ou igual a
    - ( == )  - igualdade (valor)                   OBS: não é muito utilizado
    - ( === ) - igualdade estrita (valor e tipo)
    - ( != )  - diferente (valor)                   OBS:não é muito utilizado
    - ( !== ) - diferente estrita (valor e tipo)
*/