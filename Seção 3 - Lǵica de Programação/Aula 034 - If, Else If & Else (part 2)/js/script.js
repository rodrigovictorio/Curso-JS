const numero = 10;

if (numero >= 0 && numero <= 5) {
    console.log("O número está entre 0(Zero) e 5(Cinco).");
} else if (numero >= 6 && numero <= 8) {
    console.log("O número está entre 6(Seis) e 8(Oito).");
} else if (numero >= 9 && numero <= 11) {
    console.log("O número está entre 9(nove) e 11(Onze).");
} else {
    console.log("O número não está entre 0(Zero) e 11(Onze).");
}